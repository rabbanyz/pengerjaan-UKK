    private void adActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adActionPerformed
        // TODO add your handling code here:
        int i = tblmenu.getSelectedRow();
        String harga = (String) tm.getValueAt(i,2);
        int Total = Integer.parseInt(harga);
        int qty = Integer.parseInt(Qty.getText());
        int hasil=Total*qty;
        DefaultTableModel model =(DefaultTableModel)tblorder.getModel();
        model.addRow(new Object[]{
            nmmenu.getText(), Qty.getText(),harga, hasil, tblmenu.getValueAt(i,0)});
    }//GEN-LAST:event_adActionPerformed

    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        // TODO add your handling code here:
        try{
            PreparedStatement ps = con.prepareStatement("INSERT INTO detailpenjualan VALUES(?,?,?)");
            ps.setString(1,idorder.getText());
            ps.setString(2,nmmenu.getText());
            ps.setString(3,Qty.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this,"Data telah tersimpan");
           refreshTablemenu();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null,"Data telah tersimpan!");
            System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
    }//GEN-LAST:event_saveActionPerformed

