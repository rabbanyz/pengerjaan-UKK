public void refreshTablemenu(){
    tm = new DefaultTableModel(null, new Object[]{"id_menu","nama_menu","harga","photo"});
    tblmenu.setModel(tm);
    tm.getDataVector().removeAllElements();
    try{
        PreparedStatement s = con.prepareStatement("SELECT * FROM menu");
        ResultSet r = s.executeQuery();
        while(r.next()){
            Object[] data = {
                r.getString(1),
                r.getString(2),
                r.getString(3),
                r.getString(4)
            };
            tm.addRow(data);
            }
    }catch(Exception e){
        System.out.print("ERROR KUERI KE DATABASE:\n" + e + "\n\n");
        }
}

